void PrintArray(int[,] matr)
{
    for (int rows = 0; rows < matr.GetLength(0); rows++)
    {
        for (int columns = 0; columns < matr.GetLength(1); columns++)
        {
            System.Console.Write($"{matr[rows, columns]} ");
        }
        System.Console.WriteLine();
    }
}

void FillArray(int[,] matr)
{
    for (int rows = 0; rows < matr.GetLength(0); rows++)
    {
        for (int columns = 0; columns < matr.GetLength(1); columns++)
        {
            matr[rows, columns] = new Random().Next(1, 10);
        }
    }
}

void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0) System.Console.Write(" ");
            else System.Console.Write((char)0x2b);
        }
        System.Console.WriteLine();
    }
}

void FillImage(int row, int col, int[,] image)
{
    if (image[row, col] == 0)
    {
        image[row, col] = 1;
        FillImage(row - 1, col, image);
        FillImage(row, col - 1, image);
        FillImage(row + 1, col, image);
        FillImage(row, col + 1, image);
    }
}

